/// Roast level of a coffee bean.
/// Can be one of the following:
/// 1. `veryLight`
/// 2. `light`
/// 3. `lightMedium`
/// 4. `medium`
/// 5. `darkMedium`
/// 6. `dark`
/// 7. `veryDark`
enum Roast {
  /// a _very light_ roast
  veryLight,

  /// a _light_ roast
  light,

  /// a _light-medium_ roast
  lightMedium,

  /// a _medium_ roast
  medium,

  /// a _dark-medium_ roast
  darkMedium,

  /// a _dark_ roast
  dark,

  /// a very-dark roast
  veryDark,
}
